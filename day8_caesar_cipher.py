alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

caesar_on = True

def caesar(user_text, shift_amount, direction_choice,):
    end_text = ""
    if direction_choice == "decode":
      shift_amount *= -1
    for char in user_text:
      if char in alphabet:
        new_position = abs(alphabet.index(char) + shift_amount) % 26
        new_letter = alphabet[new_position]
        end_text += new_letter
      else:
        end_text += char
    print(f"The {direction_choice}d text is {end_text}")

