  text = input("Type your message:\n").lower()
  shift = int(input("Type the shift number:\n"))
  caesar(text, shift, direction)
  continue_decision = input("Type 'yes' if you want to go again, type 'no' to quit.\n")
  if continue_decision == "no":
    caesar_on = False
